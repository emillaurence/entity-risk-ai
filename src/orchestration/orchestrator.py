    # Private helpers
    # ------------------------------------------------------------------

    def _check_stop_conditions(
        self,
        step_results: list[StepRecord],
        trace: InvestigationTrace,
    ) -> bool:
        """
        Build a risk-signal findings dict from completed steps and call
        evaluate_stop_conditions. Returns True if the investigation should halt.

        Handles both direct risk tasks and the synthesised
        summarize_risk_for_company task (which bundles all four signals).
        """
        findings: dict[str, dict] = {}

        for sr in step_results:
            if not sr.success:
                continue

            # Direct risk task (e.g. ownership_complexity_check)
            if sr.task in _RISK_SIGNAL_MAP:
                signal_key = _RISK_SIGNAL_MAP[sr.task]
                task_data = sr.findings.get(sr.task) or {}
                if isinstance(task_data, dict) and "risk_level" in task_data:
                    findings[signal_key] = {"risk_level": task_data["risk_level"]}

        if not findings:
            return False

        res = self._mcp.call_tool("evaluate_stop_conditions", {"findings": findings})
        if not res.success or not res.data:
            return False

        should_stop: bool = bool(res.data.get("should_stop", False))
        if should_stop:
            self._trace_service.add_event(
                trace,
                TraceEvent(
                    event_type=EventType.INVESTIGATION_COMPLETE,
                    message=(
                        f"Stop condition met — overall_risk="
                        f"{res.data.get('overall_risk', 'UNKNOWN')}, "
                        f"escalate={res.data.get('escalate', False)}"
                    ),
                    payload=res.data,
                ),
            )
        return should_stop


