@dataclass
class StepRecord:
    """Result record for one plan step.

    Attributes:
        step_id:        Stable identifier within the plan (e.g. "step_1").
        agent:          Agent that ran the step (e.g. "graph-agent").
        task:           Task name dispatched to the agent.
        success:        True when the agent completed without error.
        summary:        Short natural-language summary of what the step produced.
        findings:       Structured output keyed by task name.
        tools_executed: Ordered list of MCP tool names the agent called.
        error:          Error message when success is False and skipped is False.
        skipped:        True when the step was not executed (entity gate or halt).
        skip_reason:    Why the step was skipped, when skipped is True.
    """

    step_id: str
    agent: str
    task: str
    success: bool
    summary: str = ""
    findings: dict[str, Any] = field(default_factory=dict)
    tools_executed: list[str] = field(default_factory=list)
    error: str | None = None
    skipped: bool = False
    skip_reason: str | None = None

    @property
    def status(self) -> str:
        """Lifecycle status string for UI consumption.

        Derived from ``skipped`` and ``success`` so construction sites
        need not change.  "pending" and "running" are not emitted at
        runtime yet but are reserved for future async/streaming support.

        Returns one of: "success" | "failed" | "skipped".
        """
        if self.skipped:
            return "skipped"
        if self.success:
            return "success"
        return "failed"

    def to_dict(self) -> dict[str, Any]:
        return {
            "step_id":        self.step_id,
            "agent":          self.agent,
            "task":           self.task,
            "status":         self.status,
            "success":        self.success,
            "summary":        self.summary,
            "findings":       self.findings,
            "tools_executed": self.tools_executed,
            "error":          self.error,
            "skipped":        self.skipped,
            "skip_reason":    self.skip_reason,
        }


@dataclass
class OrchestratorResult:
    """
    Fully structured output from Orchestrator.run().

    Attributes:
        mode:               "investigate" or "trace".
        query:              Original user query string.
        trace_id:           Neo4j trace ID. Empty string if trace creation failed.
        success:            True when at least one step completed successfully.
        planner_output:     PlannerResult.to_dict() — mode, reason, entities, plan.
        resolved_entities:  {original_name: resolution_dict | None}.
                            None means the entity was not found in the graph.
        step_results:       Ordered StepRecord for every plan step (including skipped).
        final_answer:       Synthesised narrative drawn from step summaries.
        warnings:           Non-fatal issues (plan validation, stop-condition triggers).
        errors:             Issues that caused steps to be skipped or the planner to fail.
    """

    mode: str
    query: str
    trace_id: str
    success: bool
    planner_output: dict[str, Any]
    resolved_entities: dict[str, Any]
    step_results: list[StepRecord]
    final_answer: str
    warnings: list[str] = field(default_factory=list)
    errors: list[str] = field(default_factory=list)
    retrieved_trace_id: str = ""

    def to_dict(self) -> dict[str, Any]:
        return {
            "mode":               self.mode,
            "query":              self.query,
            "trace_id":           self.trace_id,
            "success":            self.success,
            "planner_output":     self.planner_output,
            "resolved_entities":  self.resolved_entities,
            "step_results":       [sr.to_dict() for sr in self.step_results],
            "final_answer":       self.final_answer,
            "warnings":             self.warnings,
            "errors":               self.errors,
            "retrieved_trace_id":   self.retrieved_trace_id,
        }


