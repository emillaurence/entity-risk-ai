"""
Orchestrator — investigation workflow controller.

Accepts a free-text user query, runs the LLM planner, validates the plan,
resolves entities, executes agents step-by-step, enforces stop conditions,
and returns a fully structured OrchestratorResult.

The orchestrator is NOT an agent. It is a controller:
  - It does not produce summaries itself.
  - It does not call Neo4j directly.
  - It delegates planning to InvestigationPlanner, tool calls to MCPToolClient,
    and domain work to GraphAgent / RiskAgent / TraceAgent.

Execution flow
--------------
  1. Plan      — call InvestigationPlanner.plan(query) → PlannerResult
  2. Trace     — create and persist InvestigationTrace before any agent runs
  3. Validate  — call validate_plan MCP tool for all MCP-registered steps
  4. Resolve   — call resolve_entity MCP tool for each Company-type entity
  5. Execute   — run each plan step through the appropriate agent
                   inject canonical names; gate entity-dependent steps on resolution
  6. StopCheck — after each risk step, call evaluate_stop_conditions if plan
                   carries stop_conditions; halt remaining steps if triggered
  7. Finalize  — write final summary to trace; return OrchestratorResult

Entity gating
-------------
If resolve_entity returns no match for the primary entity, all plan steps are
skipped immediately and no agents are called. The final answer is generated by
the AI client (if configured) to explain the failure to the analyst.

Trace mode
----------
Trace queries skip entity resolution entirely. trace-agent steps run unconditionally.
"""

