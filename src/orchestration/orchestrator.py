class Orchestrator:
    """
    Investigation workflow controller.

    Args:
        planner:       InvestigationPlanner for query → plan translation.
        mcp:           MCPToolClient used for validate_plan, resolve_entity,
                       and evaluate_stop_conditions.
        graph_agent:   GraphAgent instance.
        risk_agent:    RiskAgent instance.
        trace_agent:   TraceAgent instance.
        trace_service: TraceService for event logging and trace finalisation.
        trace_repo:    TraceRepository for initial trace persistence.
    """

    def __init__(
        self,
        planner: InvestigationPlanner,
        mcp: MCPToolClient,
        graph_agent: GraphAgent,
        risk_agent: RiskAgent,
        trace_agent: TraceAgent,
        trace_service: TraceService,
        trace_repo: TraceRepository,
        ai_client: AIClient | None = None,
    ) -> None:
        self._planner = planner
        self._mcp = mcp
        self._agents: dict[str, Any] = {
            "graph-agent":  graph_agent,
            "risk-agent":   risk_agent,
            "trace-agent":  trace_agent,
        }
        self._trace_service = trace_service
        self._trace_repo = trace_repo
        self._ai_client = ai_client

    # ------------------------------------------------------------------
    # Public interface
    # ------------------------------------------------------------------

    def run(
        self,
        query: str,
        user_id: str = "system",
        on_progress: Any = None,
        confirmation_queue: "_queue_mod.Queue | None" = None,
    ) -> OrchestratorResult:
        """
        Execute a full investigation from a natural-language query.

        Args:
            query:   Free-text investigation query.
            user_id: Identifier for the requesting user, written to the trace.

        Returns:
            OrchestratorResult with planner output, resolved entities,
            per-step results, final answer, and the audit trace_id.
        """
        warnings: list[str] = []
        errors: list[str] = []

        # Short ID for log correlation across all events in this run.
        _run_id = str(uuid.uuid4())
        _short  = _run_id[:8]
        _log.info("[%s] Investigation started: %r", _short, query[:120])

        # ── 1. Plan ────────────────────────────────────────────────────
        try:
            plan = self._planner.plan(query)
        except Exception as exc:
            _log.error("[%s] Planner failed: %s", _short, exc)
            return OrchestratorResult(
                mode="investigate",
                query=query,
                trace_id="",
                success=False,
                planner_output={},
                resolved_entities={},
                step_results=[],
                final_answer="",
                warnings=warnings,
                errors=[f"Planner failed: {exc}"],
            )

        _log.info(
            "[%s] Routing: mode=%s entities=%s steps=%d",
            _short, plan.mode,
            [e.name for e in plan.entities],
            len(plan.plan),
        )
        if on_progress:
            on_progress("plan_ready", plan.to_dict())

        # ── 2. Create and persist trace ────────────────────────────────
        entity_name = plan.entities[0].name if plan.entities else query
        trace = InvestigationTrace(
            request_id=_run_id,
            entity_name=entity_name,
            question=query,
            user_id=user_id,
            mode=plan.mode,
        )
        self._trace_repo.save_trace(trace)
        if on_progress:
            on_progress("trace_created", {"trace_id": trace.request_id})

        self._trace_service.add_event(
            trace,
            TraceEvent(
                event_type=EventType.PLAN_CREATED,
                message=(
                    f"Plan: mode={plan.mode}, "
                    f"{len(plan.plan)} step(s), "
                    f"reason: {plan.reason}"
                ),
                payload=plan.to_dict(),
            ),
        )

        # ── 3. Validate plan via MCP ───────────────────────────────────
        mcp_steps = [
            {"step_id": s.step_id, "tool_name": s.task}
            for s in plan.plan
            if s.task in _MCP_TOOLS
        ]
        if mcp_steps:
            val = self._mcp.call_tool("validate_plan", {"steps": mcp_steps})
            if val.success and val.data:
                for ve in val.data.get("errors") or []:
                    msg = ve.get("message", str(ve)) if isinstance(ve, dict) else str(ve)
                    warnings.append(f"Plan validation: {msg}")
            elif not val.success:
                warnings.append(f"Plan validation unavailable: {val.error}")

        # ── 4. Resolve entities ────────────────────────────────────────
        resolved: dict[str, dict | None] = {}

        if plan.mode == "investigate":
            for entity in plan.entities:
                if entity.entity_type == "Trace":
                    # Trace IDs are self-referential — no graph resolution needed.
                    resolved[entity.name] = {
                        "canonical_name": entity.name,
                        "entity_type": "Trace",
                        "resolved": True,
                    }
                    continue

                res = self._mcp.call_tool("resolve_entity", {"name": entity.name})
                if res.success and (res.data or {}).get("canonical_name"):
                    all_matches = (res.data or {}).get("all_matches", [])
                    match_count = (res.data or {}).get("match_count", 1)

                    # Compute max score for later score_pct calculation
                    _raw_scores = [float(c.get("score") or 0) for c in all_matches]
                    _max_score  = max(_raw_scores) if _raw_scores else 1.0
                    # "Perfect" = candidate name is an exact match for the searched name
                    _perfect = [
                        c for c in all_matches
                        if (c.get("name") or "").lower().strip() == entity.name.lower().strip()
                    ]

                    confirmed: dict | None = None
                    _modal_shown = False

                    if len(_perfect) == 1:
                        # Single perfect match — auto-select without prompting
                        confirmed = _perfect[0]
                    elif confirmation_queue is not None and match_count > 1:
                        # Multiple candidates → show modal
                        # Only perfect matches if any; otherwise top 5 by score
                        modal_candidates = _perfect if _perfect else sorted(
                            all_matches,
                            key=lambda c: -round(
                                _difflib.SequenceMatcher(
                                    None, entity.name.lower(), (c.get("name") or "").lower()
                                ).ratio() * 100
                            ),
                        )[:5]
                        _modal_shown = True
                        if on_progress:
                            on_progress("entity_candidates", {
                                "name": entity.name,
                                "candidates": modal_candidates,
                                "match_count": len(modal_candidates),
                            })
                        try:
                            confirmed = confirmation_queue.get(timeout=300)
                        except _queue_mod.Empty:
                            confirmed = None

                    if _modal_shown and confirmed is None:
                        _log.info(
                            "[%s] Entity selection cancelled for '%s'",
                            _short, entity.name,
                        )
                        if on_progress:
                            on_progress("error", {"error": "Investigation cancelled."})
                        return OrchestratorResult(
                            mode=plan.mode,
                            query=query,
                            trace_id=trace.request_id,
                            success=False,
                            planner_output=plan.to_dict(),
                            resolved_entities={},
                            step_results=[],
                            final_answer="Investigation was cancelled during entity selection.",
                            warnings=warnings,
                            errors=["Investigation cancelled."],
                        )

                    if confirmed is not None:
                        # Use the auto-selected or user-confirmed candidate
                        confirmed_name = confirmed.get("name") or confirmed.get("canonical_name", entity.name)
                        _score_pct = round(
                            _difflib.SequenceMatcher(
                                None, entity.name.lower(), confirmed_name.lower()
                            ).ratio() * 100
                        )
                        resolved[entity.name] = {
                            "canonical_name":  confirmed_name,
                            "company_number":  confirmed.get("company_number"),
                            "status":          confirmed.get("status"),
                            "exact_match":     confirmed_name.lower() == entity.name.lower(),
                            "match_count":     match_count,
                            "match_score_pct": _score_pct,
                            "resolved":        True,
                        }
                    else:
                        _best_name = (res.data or {}).get("canonical_name", entity.name)
                        _score_pct = round(
                            _difflib.SequenceMatcher(
                                None, entity.name.lower(), _best_name.lower()
                            ).ratio() * 100
                        )
                        resolved[entity.name] = {**res.data, "resolved": True, "match_score_pct": _score_pct}

                    canonical   = resolved[entity.name]["canonical_name"]
                    company_no  = resolved[entity.name].get("company_number", "")
                    exact_match = resolved[entity.name].get("exact_match", True)
                    _log.info(
                        "[%s] Entity resolved: '%s' → '%s' (number=%s exact=%s)",
                        _short, entity.name, canonical, company_no, exact_match,
                    )
                    if on_progress:
                        on_progress("entity_resolved", {entity.name: resolved[entity.name]})
                    self._trace_service.add_event(
                        trace,
                        TraceEvent(
                            event_type=EventType.AGENT_REASONING,
                            message=f"Resolved '{entity.name}' → '{canonical}'",
                            payload={
                                **(res.data or {}),
                                "data_json": _json.dumps({
                                    "canonical_name":  resolved[entity.name].get("canonical_name"),
                                    "company_number":  resolved[entity.name].get("company_number"),
                                    "status":          resolved[entity.name].get("status"),
                                    "match_score_pct": resolved[entity.name].get("match_score_pct"),
                                }),
                            },
                        ),
                        entity_refs=[{"label": "Company", "name": canonical}],
                    )
                else:
                    _log.warning(
                        "[%s] Entity not resolved: '%s' — %s",
                        _short, entity.name, res.error,
                    )
                    resolved[entity.name] = None
                    if on_progress:
                        on_progress("entity_resolved", {entity.name: None})
                    msg = f"Entity '{entity.name}' could not be resolved in the graph."
                    errors.append(msg)
                    self._trace_service.add_event(
                        trace,
                        TraceEvent(
                            event_type=EventType.STEP_FAILED,
                            message=msg,
                            payload={"entity": entity.name, "resolve_error": res.error},
                        ),
                    )

        # ── 5. Execute plan steps ──────────────────────────────────────
        step_results: list[StepRecord] = []

        # If the primary entity could not be resolved, skip all steps immediately.
        if plan.mode == "investigate":
            primary = next(
                (e for e in plan.entities if e.entity_type not in ("Trace",)), None
            )
            if primary and resolved.get(primary.name) is None:
                for step in plan.plan:
                    step_results.append(StepRecord(
                        step_id=step.step_id,
                        agent=step.agent,
                        task=step.task,
                        success=False,
                        skipped=True,
                        skip_reason="Entity not resolved — all steps skipped.",
                    ))

        for step in plan.plan:
            # Skip steps already recorded above (entity not resolved).
            if any(r.step_id == step.step_id for r in step_results):
                continue

            # ── Build context (inject canonical name if resolved) ──────
            context = _build_context(step, resolved)

            # ── Dispatch to agent ──────────────────────────────────────
            agent = self._agents.get(step.agent)
            if agent is None:
                msg = f"Unknown agent '{step.agent}' for step {step.step_id}."
                warnings.append(msg)
                step_results.append(StepRecord(
                    step_id=step.step_id,
                    agent=step.agent,
                    task=step.task,
                    success=False,
                    skipped=True,
                    skip_reason=msg,
                ))
                continue

            agent_label = _AGENT_DISPLAY.get(step.agent, step.agent)
            _log.info(
                "[%s] Step %s start: %s → %s",
                _short, step.step_id, agent_label, step.task,
            )
            if on_progress:
                on_progress("step_starting", {
                    "step_id": step.step_id,
                    "agent":   step.agent,
                    "task":    step.task,
                    "index":   len(step_results),
                    "n_total": len(plan.plan),
                })
            try:
                result: AgentResult = agent.run(step.task, context, trace)
            except Exception as exc:
                _log.error(
                    "[%s] Step %s error: %s → %s | %s",
                    _short, step.step_id, agent_label, step.task, exc,
                )
                result = AgentResult(
                    request_id=trace.request_id,
                    entity_name=entity_name,
                    success=False,
                    error=str(exc),
                )

            step_results.append(StepRecord(
                step_id=step.step_id,
                agent=step.agent,
                task=step.task,
                success=result.success,
                summary=result.summary,
                findings=result.findings,
                tools_executed=result.tools_used,
                error=result.error,
            ))
            if on_progress:
                on_progress("step_complete", step_results[-1].to_dict())

            _log.info(
                "[%s] Step %s complete: %s → %s | status=%s tools=%s",
                _short, step.step_id, agent_label, step.task,
                "success" if result.success else "failed",
                result.tools_used or [],
            )

            # ── Stop on failure ─────────────────────────────────────────
            if not result.success:
                remaining = plan.plan[plan.plan.index(step) + 1:]
                for rem in remaining:
                    step_results.append(StepRecord(
                        step_id=rem.step_id,
                        agent=rem.agent,
                        task=rem.task,
                        success=False,
                        skipped=True,
                        skip_reason=(
                            f"Halted — step '{step.step_id}' ({step.task}) failed."
                        ),
                    ))
                    if on_progress:
                        on_progress("step_complete", step_results[-1].to_dict())
                if remaining:
                    warnings.append(
                        f"Execution halted after '{step.step_id}' ({step.task}) failed. "
                        f"{len(remaining)} step(s) skipped."
                    )
                break

            # ── Stop condition check ────────────────────────────────────
            if plan.stop_conditions and result.success:
                should_stop = self._check_stop_conditions(step_results, trace)
                if should_stop:
                    warnings.append(
                        f"Stop condition triggered after {step.step_id}. "
                        "Remaining steps skipped."
                    )
                    break

        # ── 6. Build final answer ──────────────────────────────────────
        final_answer = _build_final_answer(step_results, self._ai_client)

        # If nothing succeeded, ask the AI to explain the failure.
        if self._ai_client and not any(sr.success for sr in step_results):
            user_prompt = (
                f"Query: {query}\n"
                f"Errors: {'; '.join(errors) if errors else 'none'}\n"
                f"Warnings: {'; '.join(warnings) if warnings else 'none'}"
            )
            try:
                ai_text = self._ai_client.generate_text(
                    system_prompt=_FAILURE_SYSTEM_PROMPT,
                    user_prompt=user_prompt,
                    max_tokens=120,
                )
                if ai_text:
                    final_answer = ai_text
            except Exception as exc:
                _log.warning("AI failure narrative unavailable: %s", exc)

        # ── 7. Finalize trace ──────────────────────────────────────────
        try:
            self._trace_service.finalize_trace(trace, final_answer)
        except Exception as exc:
            _log.error("Trace finalisation failed: %s", exc)
            warnings.append(f"Trace finalisation failed: {exc}")

        overall_success = any(sr.success for sr in step_results)
        n_done    = sum(1 for sr in step_results if sr.success)
        n_skipped = sum(1 for sr in step_results if sr.skipped)
        _log.info(
            "[%s] Investigation complete: success=%s mode=%s steps=%d/%d skipped=%d",
            _short, overall_success, plan.mode, n_done, len(step_results), n_skipped,
        )

        # Extract the target trace_id if a retrieve step succeeded.
        retrieved_trace_id = ""
        for sr in step_results:
            if not sr.success:
                continue
            if sr.task == "retrieve_trace":
                data = sr.findings.get("retrieve_trace") or {}
                retrieved_trace_id = data.get("trace_id", "")
            elif sr.task == "retrieve_and_summarize_trace":
                outer = sr.findings.get("retrieve_and_summarize_trace") or {}
                data = outer.get("trace_data") or {}
                retrieved_trace_id = data.get("trace_id", "")
            elif sr.task == "retrieve_latest_for_entity":
                outer = sr.findings.get("retrieve_latest_for_entity") or {}
                retrieved_trace_id = outer.get("latest_trace_id", "")
            if retrieved_trace_id:
                break

        return OrchestratorResult(
            mode=plan.mode,
            query=query,
            trace_id=trace.request_id,
            success=overall_success,
            planner_output=plan.to_dict(),
            resolved_entities=resolved,
            step_results=step_results,
            final_answer=final_answer,
            warnings=warnings,
            errors=errors,
            retrieved_trace_id=retrieved_trace_id,
        )

    # ------------------------------------------------------------------
    # Private helpers
    # ------------------------------------------------------------------

    def _check_stop_conditions(
        self,
        step_results: list[StepRecord],
        trace: InvestigationTrace,
    ) -> bool:
        """
        Build a risk-signal findings dict from completed steps and call
        evaluate_stop_conditions. Returns True if the investigation should halt.

        Handles both direct risk tasks and the synthesised
        summarize_risk_for_company task (which bundles all four signals).
        """
        findings: dict[str, dict] = {}

        for sr in step_results:
            if not sr.success:
                continue

            # Direct risk task (e.g. ownership_complexity_check)
            if sr.task in _RISK_SIGNAL_MAP:
                signal_key = _RISK_SIGNAL_MAP[sr.task]
                task_data = sr.findings.get(sr.task) or {}
                if isinstance(task_data, dict) and "risk_level" in task_data:
                    findings[signal_key] = {"risk_level": task_data["risk_level"]}

            # Synthesised task — findings keyed by each individual task name
            elif sr.task == "summarize_risk_for_company":
                for task_name, signal_key in _RISK_SIGNAL_MAP.items():
                    task_data = sr.findings.get(task_name) or {}
                    if isinstance(task_data, dict) and "risk_level" in task_data:
                        findings[signal_key] = {"risk_level": task_data["risk_level"]}

        if not findings:
            return False

        res = self._mcp.call_tool("evaluate_stop_conditions", {"findings": findings})
        if not res.success or not res.data:
            return False

        should_stop: bool = bool(res.data.get("should_stop", False))
        if should_stop:
            self._trace_service.add_event(
                trace,
                TraceEvent(
                    event_type=EventType.INVESTIGATION_COMPLETE,
                    message=(
                        f"Stop condition met — overall_risk="
                        f"{res.data.get('overall_risk', 'UNKNOWN')}, "
                        f"escalate={res.data.get('escalate', False)}"
                    ),
                    payload=res.data,
                ),
            )
        return should_stop


