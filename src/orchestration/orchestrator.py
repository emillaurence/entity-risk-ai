def _build_context(
    step: PlanStep,
    resolved: dict[str, dict | None],
) -> dict[str, Any]:
    """
    Build the context dict for an agent.run() call.

    Injects the canonical entity name from resolve_entity results when the
    step's company_name / name parameter matches a successfully resolved entity.
    """
    context = dict(step.parameters)
    for key in ("company_name", "name", "entity_name"):
        if key not in context:
            continue
        original = context[key]
        data = resolved.get(original)
        if data and data.get("canonical_name"):
            context[key] = data["canonical_name"]
    return context


def _build_final_answer(
    step_results: list[StepRecord],
    ai_client: AIClient | None = None,
) -> str:
    """
    Synthesise a final answer from completed step summaries.

    Priority:
      1. risk-agent summarize_risk_for_company  (richest narrative)
      2. trace-agent retrieve_and_summarize_trace / summarize_trace
      3. Individual risk task summaries — AI-synthesised if client available
      4. Last successful step summary
      5. Fallback message indicating what went wrong
    """
    successful = [sr for sr in step_results if sr.success and sr.summary]

    for sr in successful:
        if sr.task == "summarize_risk_for_company":
            return sr.summary

    for sr in successful:
        if sr.task in ("retrieve_and_summarize_trace", "summarize_trace"):
            return sr.summary

    _INDIVIDUAL_RISK_TASKS = frozenset({
        "address_risk_check",
        "control_signal_check",
        "ownership_complexity_check",
        "industry_context_check",
    })
    risk_summaries = [
        sr.summary
        for sr in successful
        if sr.task in _INDIVIDUAL_RISK_TASKS
    ]
    if risk_summaries:
        joined = " ".join(risk_summaries)
        if ai_client:
            try:
                synthesized = ai_client.generate_text(
                    system_prompt=_INDIVIDUAL_RISK_SYNTHESIS_PROMPT,
                    user_prompt=joined,
                    max_tokens=150,
                )
                if synthesized:
                    return synthesized
                _log.warning("_build_final_answer: AI synthesis returned empty string")
            except Exception as exc:
                _log.warning("_build_final_answer: AI synthesis failed (%s), using raw join", exc)
        return joined

    if successful:
        return successful[-1].summary

    # Nothing succeeded — explain why
    skipped = [sr for sr in step_results if sr.skipped]
    if skipped:
        return (
            "Investigation could not complete: the requested entity was not found "
            "in the graph. Verify the company name and try again."
        )

    errs = [sr.error for sr in step_results if sr.error]
    if errs:
        return f"Investigation failed: {errs[0]}"

    return "No results were obtained for this query."
